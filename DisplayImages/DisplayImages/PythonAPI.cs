                    if (exception != "")
                    {
                        StreamWriter write = new StreamWriter(path + "/DisplayImages/error.txt");
                        DevConsole.Log(DCSection.Mod, "|RED| something went wrong. Check error.txt for more details");
                        write.WriteLine(exception);
                        write.Close();
                    }
                }
            }
        }
    }
